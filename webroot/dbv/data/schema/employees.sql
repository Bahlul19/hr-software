CREATE TABLE `employees` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `first_name` varchar(50) NOT NULL,
  `last_name` varchar(50) NOT NULL,
  `emp_id` varchar(50) NOT NULL,
  `personal_email` varchar(50) DEFAULT NULL,
  `office_email` varchar(50) NOT NULL,
  `password` varchar(250) NOT NULL,
  `present_address` text,
  `permanent_address` text NOT NULL,
  `mobile_number` int(20) NOT NULL,
  `alternate_number` int(20) NOT NULL,
  `emergency_number` int(20) NOT NULL,
  `country` varchar(50) NOT NULL,
  `office_location` varchar(50) NOT NULL,
  `gender` tinyint(3) NOT NULL,
  `birth_date` date NOT NULL,
  `maritial_status` varchar(20) DEFAULT NULL,
  `identity_proof` text NOT NULL,
  `blood_group` char(4) DEFAULT NULL,
  `nid_no` int(20) DEFAULT NULL,
  `bank_account_number` varchar(20) DEFAULT NULL,
  `salary` int(20) unsigned DEFAULT NULL,
  `tax_bracket` varchar(50) CHARACTER SET utf8 NOT NULL,
  `languages` text,
  `max_qualification` varchar(100) NOT NULL,
  `designation_id` int(11) unsigned NOT NULL,
  `shift_type` varchar(20) NOT NULL,
  `department_id` int(11) unsigned DEFAULT NULL,
  `reporting_manager` varchar(30) CHARACTER SET utf8 DEFAULT NULL,
  `leave_approver` varchar(30) CHARACTER SET utf8 NOT NULL,
  `mentor` varchar(30) CHARACTER SET utf8 DEFAULT NULL,
  `date_of_joining` date NOT NULL,
  `anniversary` date DEFAULT NULL,
  `source_of_hire` varchar(20) DEFAULT NULL,
  `referred_by` varchar(30) DEFAULT NULL,
  `employment_status` varchar(10) NOT NULL,
  `work_phone` varchar(20) NOT NULL,
  `employment_type` varchar(20) NOT NULL,
  `confirmation_date` date NOT NULL,
  `designation_change` int(11) unsigned DEFAULT NULL,
  `designation_change_date` date NOT NULL,
  `increment_date` date DEFAULT NULL,
  `resignation_date` date NOT NULL,
  `last_working_date` date NOT NULL,
  `notice_period` varchar(20) NOT NULL,
  `reason` varchar(30) CHARACTER SET utf8 DEFAULT NULL,
  `blacklisted` varchar(30) CHARACTER SET utf8 DEFAULT NULL,
  `notes` text CHARACTER SET utf8,
  `knowledge` text NOT NULL,
  `level` varchar(20) NOT NULL,
  `created` datetime DEFAULT NULL,
  `modified` datetime DEFAULT NULL,
  PRIMARY KEY (`id`),
  KEY `department_id` (`department_id`),
  KEY `designation_id` (`designation_id`),
  CONSTRAINT `employees_ibfk_1` FOREIGN KEY (`designation_id`) REFERENCES `designations` (`id`),
  CONSTRAINT `employees_ibfk_2` FOREIGN KEY (`department_id`) REFERENCES `departments` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1